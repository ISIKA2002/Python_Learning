#Integer_datatype
a= 1
print("Integer value:", a)
print(type(a))

#Float_datatype
b= 1.0
print("Float value:", b)
print(type(b))

#Complex_value
c= 1+2j
print("Complex value:", c)
print(type(c))

#String_datatype
d= "John"
print("String value:", d)
print(type(d))

#Boolean_datatype
e= True
print("Boolean value:", e)
print(type(e))

#List_datatype
f=["a", "b", "c", "d"]
print("List value:", f)
print(type(f))

#Tuple_datatype
g=("a", 1, True)
print("Tuple value:", g)
print(type(g))

#Dictionary_datatype
h= {"name" : "John", "age" : 50}
print("Dictionary value:", h)
print(type(h))

#Set_datatype
i={"apple", "banana", "mango"}
print("Set value:", i)
print(type(i))
